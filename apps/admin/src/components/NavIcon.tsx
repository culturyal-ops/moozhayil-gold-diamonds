function initialsFromName(name: string): string {
  return name
    .split(/\s+/)
    .filter(Boolean)
    .slice(0, 2)
    .map((part) => part[0]?.toUpperCase() ?? "")
    .join("");
}

export function UserAvatar({ name }: { name: string }) {
  return <span className="user-avatar">{initialsFromName(name) || "M"}</span>;
}
